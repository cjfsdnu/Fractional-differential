% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
str=get(handles.popupmenu1,'value');
data_from_pushbutton1=get(handles.pushbutton1,'userdata');
band=data_from_pushbutton1{2};
data=data_from_pushbutton1{1};
switch str
    case 1
        
    case 2
        data=log(data);
    case 3
        data=1./data;
    case 4
        data=1./log(data);
end
add_data=zeros(size(data,1),21);
x=band(1:76);
for i=1:size(data,1)
    y=data(i,1:76);
    [fit,~]=createFit(x, y);
    add_data(i,:)=fit(band(1)-1:-1:band(1)-21);
end
data=[add_data,data];
vs=str2double(get(handles.edit2,'string'));
ve=str2double(get(handles.edit4,'string'));
dv=str2double(get(handles.edit5,'string'));
v=vs:dv:ve;
% assignin('base','v',v);
if isnan(v)
    errordlg('需要输入阶数','警告');
else
    out=cell(1,length(v));
    for i=1:length(v)
        for j=1:size(data,1)
            out{i}(j,:)=glfdiff(data(j,:),1,v(i));
        end
    end
    flag=1;
    set(handles.pushbutton3,'userdata',{out,flag});
    msgbox('计算完成');
end
% assignin('base','data',data);


